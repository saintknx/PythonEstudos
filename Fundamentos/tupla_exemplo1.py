lanche = ('Hambúrguer', 'Suco', 'Pizza', 'Pudim') # Iniciada com ou sem parentêses - OPCIONAL
print(lanche) # Tupla inteira
print(lanche[1]) # Suco
print(lanche[-2]) # Pízza
print(lanche[1:3]) # Elemento 3 ignorado = fatiamento
print(lanche[2:]) # Pizza, Pudim
print(lanche[:2]) # Hamburguer, Suco
print(lanche[-2:]) # Pizza, Pudim
print(lanche[-3:]) #Suco, pizza, pudim