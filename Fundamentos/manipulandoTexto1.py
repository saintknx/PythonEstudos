frase = 'Curso em Vídeo Python'
print(frase[0]) # C  Primeira letra
print(frase[9:21]) # Vídeo Python Do índice 9 ao 21
print(frase[9:21:2]) # VdoPto Do índice 9 ao 21 pulando de 2 em 2
print(frase[:5]) # Curso Do início ao índice 5
print(frase[15:]) # Python Do índice 15 até o final
print(frase[9::3]) # VePh Do índice 9 até o final pulando de 3 em 3